// ─── Hook ──────────────────────────────────────────────────────────────────────
// Reads locale directly from the URL path — no next-intl context needed.
// Works on every navigation: /fr/... = French, /en/... = English.
export function useT(): TextBundle {
  const pathname = usePathname()
  const locale: Locale = pathname.startsWith("/en") ? "en" : "fr"
  return TEXT[locale]
}
